        static constexpr bool is_vector = size > 1;

        /// vector type, like __m128i for sse 4x integer vector
        using vector_t = __m128;

        /// scalar type, like int for sse 4x integer vector
        using element_t = float;

        /// mask type for boolean operations
        using mask_vector_t = __m128;

        /// extracted std::array of (dim) scalar values
        using extracted_t = std::array<element_t, size>;

        /**
         * copy constructor
         * @tparam T any type convertable to Vector
         * @param other
         */
        template<typename T, typename = std::enable_if_t<std::is_convertible<T, __m128>::value>>// || std::is_convertible<T, float>::value>>
        constexpr zfloat32(const T& other) noexcept
                : float32_detail::zval_base<FeatureMask>(other)
        {}

        /**
         * move constructor
         * @tparam T any type convertable to Vector
         * @param other
         */
        template<typename T, typename = std::enable_if_t<(size > 1) && std::is_convertible<T, __m128>::value>>
        constexpr zfloat32(T&& other) noexcept
            : float32_detail::zval_base<FeatureMask>(std::forward<T>(other))
        {}

        /**
         * copy constructor
         * @param other
         */
        constexpr zfloat32(const bfloat32<FeatureMask>& other) noexcept
            : float32_detail::zval_base<FeatureMask>(other.value())
        {}


